#include <stdio.h>

int main() {
    int x; // Змінна оголошена, але не ініціалізована

    printf("Значення x: %d\n", x); // Використання до ініціалізації

    // Щоб уникнути попереджень компілятора, можна явно "інактивно" використовувати x
    if (x > -1000000) { // Це просто умовна перевірка
        printf("x більше за -1000000\n");
    }

    return 0;
}
