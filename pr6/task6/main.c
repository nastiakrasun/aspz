#include <stdio.h>

int main() {
    int a[3] = {1, 2, 3};

    // Переповнення масиву при читанні з декількох індексів поза межами масиву
    for (int i = 0; i < 5; i++) {
        printf("Зчитано: %d з a[%d]\n", a[i], i);  // Це викликає доступ до a[3], a[4] — неініціалізовані індекси
    }

    return 0;
}

